import React, { useEffect, useMemo, useState } from 'react';
import { formatInstant, formatMinorAmount } from '../utils/format.js';
import { EnumBadge } from './EnumBadge.js';
export function CaseDetailPanel({ selectedCaseId, caseStatuses, detail, loading, error, statusBusy, noteBusy, statusFlash, noteFlash, onRetry, onSaveStatus, onAddNote }) {
    const [nextStatus, setNextStatus] = useState('OPEN');
    const [noteBody, setNoteBody] = useState('');
    useEffect(() => {
        if (detail?.caseStatus) {
            setNextStatus(detail.caseStatus);
        }
    }, [detail?.caseStatus]);
    useEffect(() => {
        setNoteBody('');
    }, [detail?.caseId]);
    const amountDelta = useMemo(() => {
        if (!detail?.settlementRow || !detail.payBridgeSnapshot) {
            return null;
        }
        return detail.settlementRow.amountMinor - detail.payBridgeSnapshot.amountMinor;
    }, [detail]);
    if (!selectedCaseId) {
        return (React.createElement("section", { className: "card empty-panel" },
            React.createElement("div", { className: "empty-state" }, "Select a case from the queue to load the compare view, linked PayBridge context, and operator note workflow.")));
    }
    if (loading) {
        return (React.createElement("section", { className: "card empty-panel" },
            React.createElement("div", { className: "empty-state" }, "Loading the selected case detail\u2026")));
    }
    if (error) {
        return (React.createElement("section", { className: "card stack" },
            React.createElement("div", { className: "alert error" }, error),
            React.createElement("div", null,
                React.createElement("button", { type: "button", className: "secondary-button", onClick: onRetry }, "Retry case detail"))));
    }
    if (!detail) {
        return (React.createElement("section", { className: "card empty-panel" },
            React.createElement("div", { className: "empty-state" }, "The selected case could not be loaded.")));
    }
    const settlementAmount = detail.settlementRow
        ? formatMinorAmount(detail.settlementRow.amountMinor, detail.settlementRow.currency)
        : '—';
    const snapshotAmount = detail.payBridgeSnapshot
        ? formatMinorAmount(detail.payBridgeSnapshot.amountMinor, detail.payBridgeSnapshot.currency)
        : '—';
    const deltaDisplay = amountDelta === null
        ? '—'
        : formatMinorAmount(Math.abs(amountDelta), detail.settlementRow?.currency ?? detail.payBridgeSnapshot?.currency);
    const deltaLabel = amountDelta === null
        ? 'No comparable amounts'
        : amountDelta === 0
            ? 'Amounts match'
            : amountDelta > 0
                ? `Settlement is higher by ${deltaDisplay}`
                : `PayBridge is higher by ${deltaDisplay}`;
    return (React.createElement("section", { className: "card detail-shell" },
        React.createElement("div", { className: "detail-header" },
            React.createElement("div", null,
                React.createElement("div", { className: "inline-badges" },
                    React.createElement(EnumBadge, { kind: "type", value: detail.caseType }),
                    React.createElement(EnumBadge, { kind: "status", value: detail.caseStatus }),
                    React.createElement(EnumBadge, { kind: "provider", value: detail.provider })),
                React.createElement("h2", { style: { marginTop: '0.75rem' } }, detail.summary),
                React.createElement("p", { className: "muted" },
                    "Opened ",
                    formatInstant(detail.openedAt),
                    " \u00B7 Match key ",
                    React.createElement("span", { className: "mono" }, detail.matchKey || 'none'))),
            React.createElement("div", { className: "subcard" },
                React.createElement("div", { className: "key-value" },
                    React.createElement("div", null,
                        React.createElement("dt", null, "Case ID"),
                        React.createElement("dd", { className: "mono" }, detail.caseId)),
                    React.createElement("div", null,
                        React.createElement("dt", null, "Run ID"),
                        React.createElement("dd", { className: "mono" }, detail.runId)),
                    React.createElement("div", null,
                        React.createElement("dt", null, "Resolved"),
                        React.createElement("dd", null, formatInstant(detail.resolvedAt)))))),
        React.createElement("div", { className: "stat-grid" },
            React.createElement("div", { className: "stat-card" },
                React.createElement("div", { className: "label" }, "Settlement amount"),
                React.createElement("div", { className: "metric" }, settlementAmount)),
            React.createElement("div", { className: "stat-card" },
                React.createElement("div", { className: "label" }, "PayBridge amount"),
                React.createElement("div", { className: "metric" }, snapshotAmount)),
            React.createElement("div", { className: "stat-card" },
                React.createElement("div", { className: "label" }, "Comparison"),
                React.createElement("div", { className: "metric", style: { fontSize: '1rem' } }, deltaLabel))),
        React.createElement("div", { className: "mutation-row" },
            React.createElement("form", { className: "subcard stack", onSubmit: (event) => {
                    event.preventDefault();
                    onSaveStatus(nextStatus);
                } },
                React.createElement("div", { className: "section-header compact" },
                    React.createElement("div", null,
                        React.createElement("h3", null, "Update status"),
                        React.createElement("p", { className: "muted" }, "Keep the case workflow intentionally small: operator review, resolution, or ignore."))),
                statusFlash ? React.createElement("div", { className: `alert ${statusFlash.tone === 'success' ? 'success' : 'error'}` }, statusFlash.text) : null,
                React.createElement("label", null,
                    React.createElement("span", null, "Case status"),
                    React.createElement("select", { value: nextStatus, onChange: (event) => setNextStatus(event.target.value) }, caseStatuses.map((status) => (React.createElement("option", { key: status, value: status }, status))))),
                React.createElement("div", null,
                    React.createElement("button", { type: "submit", disabled: statusBusy }, statusBusy ? 'Saving…' : 'Save status'))),
            React.createElement("form", { className: "subcard stack note-form", onSubmit: (event) => {
                    event.preventDefault();
                    onAddNote(noteBody);
                } },
                React.createElement("div", { className: "section-header compact" },
                    React.createElement("div", null,
                        React.createElement("h3", null, "Add operator note"),
                        React.createElement("p", { className: "muted" }, "Capture what was checked, whether PayBridge context matched, and what follow-up is still open."))),
                noteFlash ? React.createElement("div", { className: `alert ${noteFlash.tone === 'success' ? 'success' : 'error'}` }, noteFlash.text) : null,
                React.createElement("label", null,
                    React.createElement("span", null, "Note body"),
                    React.createElement("textarea", { value: noteBody, placeholder: "Summarize what you checked and what the next operator should know.", onChange: (event) => setNoteBody(event.target.value) })),
                React.createElement("div", null,
                    React.createElement("button", { type: "submit", disabled: noteBusy || noteBody.trim().length === 0 }, noteBusy ? 'Saving…' : 'Add note')))),
        React.createElement("div", { className: "compare-grid" },
            React.createElement("section", { className: "subcard stack" },
                React.createElement("div", { className: "section-header compact" },
                    React.createElement("div", null,
                        React.createElement("h3", null, "Settlement row"),
                        React.createElement("p", { className: "muted" }, "The imported external settlement source for this discrepancy."))),
                !detail.settlementRow ? React.createElement("div", { className: "empty-state" }, "This case is not linked to a settlement row.") : (React.createElement(React.Fragment, null,
                    React.createElement("dl", { className: "key-value inline-grid" },
                        React.createElement("div", null,
                            React.createElement("dt", null, "Row"),
                            React.createElement("dd", null, detail.settlementRow.rowNumber)),
                        React.createElement("div", null,
                            React.createElement("dt", null, "Provider"),
                            React.createElement("dd", null, detail.settlementRow.provider || '—')),
                        React.createElement("div", null,
                            React.createElement("dt", null, "Order ID"),
                            React.createElement("dd", null, detail.settlementRow.orderId || '—')),
                        React.createElement("div", null,
                            React.createElement("dt", null, "Provider payment"),
                            React.createElement("dd", { className: "mono" }, detail.settlementRow.providerPaymentId || '—')),
                        React.createElement("div", null,
                            React.createElement("dt", null, "Provider transaction"),
                            React.createElement("dd", { className: "mono" }, detail.settlementRow.providerTransactionId || '—')),
                        React.createElement("div", null,
                            React.createElement("dt", null, "Settled at"),
                            React.createElement("dd", null, formatInstant(detail.settlementRow.settledAt))),
                        React.createElement("div", null,
                            React.createElement("dt", null, "Amount"),
                            React.createElement("dd", null, settlementAmount)),
                        React.createElement("div", null,
                            React.createElement("dt", null, "Row ID"),
                            React.createElement("dd", { className: "mono" }, detail.settlementRow.settlementRowId))),
                    detail.settlementRow.rawRowJson ? (React.createElement("details", null,
                        React.createElement("summary", null, "Raw row JSON"),
                        React.createElement("pre", { className: "code-block" }, detail.settlementRow.rawRowJson))) : null))),
            React.createElement("section", { className: "subcard stack" },
                React.createElement("div", { className: "section-header compact" },
                    React.createElement("div", null,
                        React.createElement("h3", null, "PayBridge snapshot"),
                        React.createElement("p", { className: "muted" }, "The machine-readable export row captured during the manual reconciliation run."))),
                !detail.payBridgeSnapshot ? React.createElement("div", { className: "empty-state" }, "This case is not linked to a PayBridge snapshot.") : (React.createElement("dl", { className: "key-value inline-grid" },
                    React.createElement("div", null,
                        React.createElement("dt", null, "Payment ID"),
                        React.createElement("dd", { className: "mono" }, detail.payBridgeSnapshot.paymentId)),
                    React.createElement("div", null,
                        React.createElement("dt", null, "Status"),
                        React.createElement("dd", null, detail.payBridgeSnapshot.status || '—')),
                    React.createElement("div", null,
                        React.createElement("dt", null, "Order ID"),
                        React.createElement("dd", null, detail.payBridgeSnapshot.orderId || '—')),
                    React.createElement("div", null,
                        React.createElement("dt", null, "Provider"),
                        React.createElement("dd", null, detail.payBridgeSnapshot.provider || '—')),
                    React.createElement("div", null,
                        React.createElement("dt", null, "Provider payment"),
                        React.createElement("dd", { className: "mono" }, detail.payBridgeSnapshot.providerPaymentId || '—')),
                    React.createElement("div", null,
                        React.createElement("dt", null, "Provider transaction"),
                        React.createElement("dd", { className: "mono" }, detail.payBridgeSnapshot.providerTransactionId || '—')),
                    React.createElement("div", null,
                        React.createElement("dt", null, "Approved at"),
                        React.createElement("dd", null, formatInstant(detail.payBridgeSnapshot.approvedAt))),
                    React.createElement("div", null,
                        React.createElement("dt", null, "Created at"),
                        React.createElement("dd", null, formatInstant(detail.payBridgeSnapshot.createdAt))),
                    React.createElement("div", null,
                        React.createElement("dt", null, "Updated at"),
                        React.createElement("dd", null, formatInstant(detail.payBridgeSnapshot.updatedAt))),
                    React.createElement("div", null,
                        React.createElement("dt", null, "Amount"),
                        React.createElement("dd", null, snapshotAmount)),
                    React.createElement("div", null,
                        React.createElement("dt", null, "Reversible amount"),
                        React.createElement("dd", null, formatMinorAmount(detail.payBridgeSnapshot.reversibleAmountMinor, detail.payBridgeSnapshot.currency))))))),
        React.createElement("section", { className: "subcard stack" },
            React.createElement("div", { className: "section-header compact" },
                React.createElement("div", null,
                    React.createElement("h3", null, "PayBridge operator context"),
                    React.createElement("p", { className: "muted" }, "The browser never calls PayBridge directly. The recon backend aggregates detail, audit logs, and outbox events into this single case response."))),
            !detail.payBridgeContext ? React.createElement("div", { className: "empty-state" }, "No PayBridge context is attached to this case.") : (React.createElement(React.Fragment, null,
                detail.payBridgeContext.errorSummary ? React.createElement("div", { className: "callout error" }, detail.payBridgeContext.errorSummary) : null,
                detail.payBridgeContext.paymentDetail ? (React.createElement("div", { className: "stack" },
                    React.createElement("dl", { className: "key-value inline-grid" },
                        React.createElement("div", null,
                            React.createElement("dt", null, "Payment status"),
                            React.createElement("dd", null, detail.payBridgeContext.paymentDetail.status || '—')),
                        React.createElement("div", null,
                            React.createElement("dt", null, "Display amount"),
                            React.createElement("dd", null, detail.payBridgeContext.paymentDetail.amountDisplay)),
                        React.createElement("div", null,
                            React.createElement("dt", null, "Reversible amount"),
                            React.createElement("dd", null, detail.payBridgeContext.paymentDetail.reversibleAmountDisplay)),
                        React.createElement("div", null,
                            React.createElement("dt", null, "Approved at"),
                            React.createElement("dd", null, detail.payBridgeContext.paymentDetail.approvedAtDisplay || '—')),
                        React.createElement("div", null,
                            React.createElement("dt", null, "Full reversal allowed"),
                            React.createElement("dd", null, detail.payBridgeContext.paymentDetail.fullReversalAllowed ? 'Yes' : 'No')),
                        React.createElement("div", null,
                            React.createElement("dt", null, "Partial reversal allowed"),
                            React.createElement("dd", null, detail.payBridgeContext.paymentDetail.partialReversalAllowed ? 'Yes' : 'No'))),
                    detail.payBridgeContext.paymentDetail.reversals.length > 0 ? (React.createElement("div", { className: "table-scroll" },
                        React.createElement("table", null,
                            React.createElement("thead", null,
                                React.createElement("tr", null,
                                    React.createElement("th", null, "Reversal type"),
                                    React.createElement("th", null, "Status"),
                                    React.createElement("th", null, "Amount"),
                                    React.createElement("th", null, "Processed"))),
                            React.createElement("tbody", null, detail.payBridgeContext.paymentDetail.reversals.map((reversal) => (React.createElement("tr", { key: reversal.reversalId },
                                React.createElement("td", null, reversal.reversalType),
                                React.createElement("td", null, reversal.status),
                                React.createElement("td", null, reversal.amountDisplay),
                                React.createElement("td", null, reversal.processedAtDisplay || '—')))))))) : null)) : React.createElement("div", { className: "empty-state" }, "No upstream payment detail was returned."),
                React.createElement("div", { className: "stack" },
                    React.createElement("div", null,
                        React.createElement("h4", null, "Audit logs"),
                        detail.payBridgeContext.auditLogs.length === 0 ? React.createElement("div", { className: "empty-state" }, "No audit logs returned.") : (React.createElement("div", { className: "table-scroll" },
                            React.createElement("table", null,
                                React.createElement("thead", null,
                                    React.createElement("tr", null,
                                        React.createElement("th", null, "Occurred"),
                                        React.createElement("th", null, "Action"),
                                        React.createElement("th", null, "Outcome"),
                                        React.createElement("th", null, "Message"))),
                                React.createElement("tbody", null, detail.payBridgeContext.auditLogs.map((log) => (React.createElement("tr", { key: log.id },
                                    React.createElement("td", null, formatInstant(log.occurredAt)),
                                    React.createElement("td", null, log.action),
                                    React.createElement("td", null, log.outcome),
                                    React.createElement("td", null,
                                        React.createElement("div", null, log.message || '—'),
                                        log.correlationId ? React.createElement("div", { className: "muted mono" }, log.correlationId) : null,
                                        log.detailJson ? (React.createElement("details", null,
                                            React.createElement("summary", null, "detail json"),
                                            React.createElement("pre", { className: "code-block" }, log.detailJson))) : null))))))))),
                    React.createElement("div", null,
                        React.createElement("h4", null, "Outbox events"),
                        detail.payBridgeContext.outboxEvents.length === 0 ? React.createElement("div", { className: "empty-state" }, "No outbox events returned.") : (React.createElement("div", { className: "table-scroll" },
                            React.createElement("table", null,
                                React.createElement("thead", null,
                                    React.createElement("tr", null,
                                        React.createElement("th", null, "Created"),
                                        React.createElement("th", null, "Event type"),
                                        React.createElement("th", null, "Status"),
                                        React.createElement("th", null, "Retries"),
                                        React.createElement("th", null, "Context"))),
                                React.createElement("tbody", null, detail.payBridgeContext.outboxEvents.map((event) => (React.createElement("tr", { key: event.id },
                                    React.createElement("td", null, formatInstant(event.createdAt)),
                                    React.createElement("td", null, event.eventType),
                                    React.createElement("td", null, event.status),
                                    React.createElement("td", null, event.retryCount),
                                    React.createElement("td", null,
                                        React.createElement("div", { className: "muted mono" },
                                            "aggregate ",
                                            event.aggregateType || '—',
                                            " / ",
                                            event.aggregateId || '—'),
                                        event.lastError ? React.createElement("div", { className: "callout warning" }, event.lastError) : null,
                                        event.payloadJson ? (React.createElement("details", null,
                                            React.createElement("summary", null, "payload json"),
                                            React.createElement("pre", { className: "code-block" }, event.payloadJson))) : null))))))))))))),
        React.createElement("section", { className: "subcard stack" },
            React.createElement("div", { className: "section-header compact" },
                React.createElement("div", null,
                    React.createElement("h3", null, "Operator notes"),
                    React.createElement("p", { className: "muted" }, "Notes stay intentionally small: author, timestamp, and plain text body.")),
                React.createElement("span", { className: "badge" },
                    detail.notes.length,
                    " notes")),
            detail.notes.length === 0 ? React.createElement("div", { className: "empty-state" }, "No notes have been recorded for this case yet.") : (React.createElement("div", { className: "timeline" }, detail.notes.map((note) => (React.createElement("article", { key: note.noteId, className: "timeline-note" },
                React.createElement("div", { className: "section-header compact" },
                    React.createElement("strong", null, note.author),
                    React.createElement("span", { className: "muted" }, formatInstant(note.createdAt))),
                React.createElement("p", { className: "timeline-note-body" }, note.body)))))))));
}
